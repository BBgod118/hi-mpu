static void dal_cal_crc(unsigned char *p_data, unsigned int length, unsigned int *crc)
{
    unsigned char uc_crc_hi = 0xFF; /* CRC 高字节的初始化 */
    unsigned char uc_crc_lo = 0xFF; /* CRC 低字节的初始化 */
    unsigned char uc_index;        /* CRC 查找表的指针 */
    unsigned int len = length;

    if ((p_data == NULL) || (crc == NULL) || (length == 0)) {
        printf("CRC-16: parameter error.");
        return;
    }

    while (len-- > 0) {
        uc_index = (unsigned char)(uc_crc_hi ^ (*p_data++));
        /* 计算CRC */
        uc_crc_hi = (unsigned char)(uc_crc_lo ^ g_crc_table_lo[uc_index]);
        uc_crc_lo = g_crc_table_hi[uc_index];
    }
    *crc = (unsigned int)(uc_crc_lo | ((unsigned int)uc_crc_hi << 8)); /* uc_crc_hi take high 8 bits */
    return;
}

static int tx_and_rx_msg(unsigned int data_length, sec_set_bmc_boot_ok_req_s *user_data_out, int fd)
{
    static unsigned char g_buf_in[BUF_SIZE];
    uintptr_t buffer_addr;
    sec_module_cmd_s msg_data;
    sec_common_msg_resp_s *user_data_in;
    unsigned short crc_16;
    unsigned int data_len = data_length;

    buffer_addr = (unsigned long long)g_buf_out + data_len;
    dal_cal_crc((unsigned char *)user_data_out, (unsigned short)data_len, (unsigned short *)buffer_addr);
    data_len += sizeof(user_data_out->crc_16);

    msg_data.s_len = data_len;
    msg_data.s_buff = (unsigned char *)g_buf_out;
    msg_data.r_buff = (unsigned char *)g_buf_in;
    msg_data.r_len = BUF_SIZE;

    if (ioctl(fd, SEC_MODULE_CMD_TX_RX, &msg_data) < 0) {
        return -1;
    }

    user_data_in = (sec_common_msg_resp_s *)g_buf_in;

    if (msg_data.r_len < MIN_RSP_LEN) {
        return -1;
    }

    dal_cal_crc(g_buf_in, (unsigned short)(msg_data.r_len - sizeof(user_data_in->crc_16)), &crc_16);
    buffer_addr = (unsigned long long)g_buf_in + msg_data.r_len - sizeof(user_data_in->crc_16);
    if (crc_16 != *(unsigned short *)buffer_addr) {
        return -1;
    }
    
    return 0;
}

static int coremsg_bootok(void)
{
    int fd = 0;
    int ret = 0;
    sec_set_bmc_boot_ok_req_s *user_data_out;
    unsigned int data_len = 0;

    fd = open(SEC_MODULE_DEVNAME, O_RDWR);
    if (fd < 0) {
        ret = -1;
        goto exit;
    }

    user_data_out = (sec_set_bmc_boot_ok_req_s *)g_buf_out;
    user_data_out->hd.cmd = MSG_CMD_A55_SEC_FW;
    user_data_out->hd.sub_cmd = FW_MSG_BMC_BOOT_OK;

    data_len += MSG_USER_HEAD_LENGTH;

    ret = tx_and_rx_msg(data_len, user_data_out, fd);
exit:
    close(fd);
    return ret;
}

static int fw_upgrade_set_update_flag(void)
{
    int fd = 0;
    int ret = 0;
    int flag = 0xF;
    sec_set_upgrade_flag_req_s *user_data_out;
    sec_common_msg_resp_s *user_data_in;
    sec_module_cmd_s msg_data;
    uintptr_t buffer_addr;
    unsigned int data_len = 0;
    unsigned short crc_16;

    fd = open(SEC_MODULE_DEVNAME, O_RDWR);
    if (fd < 0) {
        return -1;
    }

    user_data_out = (sec_set_upgrade_flag_req_s *)g_buf_out;
    user_data_out->hd.cmd = MSG_CMD_A55_SEC_FW;
    user_data_out->hd.sub_cmd = FW_MSG_SET_UPGRADE_FLAG;

    data_len += MSG_USER_HEAD_LENGTH;

    user_data_out->flag = (unsigned short)flag;
    data_len += sizeof(user_data_out->flag);

    ret = tx_and_rx_msg(data_len, user_data_out, fd);
exit:
    close(fd);
    return ret;
}

int main(int argc, char *argv[])
{
    int ret = -1;
    if ((argc == ARGC_NUM) && strcmp(argv[1], "bootok") == 0) {
        ret = coremsg_bootok();
        if (ret != 0) {
            printf("secfw set bmcBootOk fail\n");
        }
    } else if ((argc == ARGC_NUM) && strcmp(argv[1], "setUpdFlag") == 0) {
        ret = fw_upgrade_set_update_flag();
        if (ret != 0) {
            printf("secfw set UpdFlag fail\n");
        }
    }
    return ret;
}
